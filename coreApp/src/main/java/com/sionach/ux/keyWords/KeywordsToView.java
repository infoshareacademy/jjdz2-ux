public class KeywordsToView {

    private String googleLinkToKeyword;
    private String keyword;
    private String glyphicon;

    public String getGoogleLinkToKeyword() {
        return googleLinkToKeyword;
    }

    public void setGoogleLinkToKeyword(String googleLinkToKeyword) {
        this.googleLinkToKeyword = googleLinkToKeyword;
    }

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public String getGlyphicon() {
        return glyphicon;
    }

    public void setGlyphicon(String glyphicon) {
        this.glyphicon = glyphicon;
    }



}
